# Define toCsvString() on our IrisData type so it works
# with the generic addData[T]().
proc toCsvString(data: IrisData): string =
    result &= "sepalLength,sepalWidth,petalLength,petalWidth,class"
    for i in 0 ..< data.sepalLength.len:
        result &= &"\n{data.sepalLength[i]},{data.sepalWidth[i]},{data.petalLength[i]},{data.petalWidth[i]},\"{data.class[i]}\""


