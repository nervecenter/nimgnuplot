var g = initGnuplotScript()

g.cmd """
    set terminal svg size 900,900 dynamic background rgb 'white'
    set style fill solid 1.0
    set multiplot layout 4,4 margins screen 0.1,0.9,0.1,0.9 spacing screen 0.02 title 'Iris Data' font ',20'
    unset xlabel
    unset ylabel
    unset xtics
    unset ytics
    set link y2
"""

let colHeaders = g.addData("iris", iris)
var displayedKey = false

for i, propI in PROPERTIES:
    for j, propJ in PROPERTIES:
        g.cmd """
            unset xtics
            unset ytics
            unset x2tics
            unset y2tics
        """
        
        # Set the intermittent axis tics
        if i == 0:
            if j == 1 or j == 3:
                g.cmd "set x2tics out"
        if j == 0:
            if i == 1 or i == 3:
                g.cmd "set ytics out nomirror rotate by 90"
            if i == 3:
                g.cmd "set xtics out nomirror"
        if i == 3:
            if j == 2:
                g.cmd "set xtics out nomirror"
        if j == 3:
            if i == 0 or i == 2:
                g.cmd "set y2tics out rotate by 90"

        # Set the diagonal plots to display the property names
        if i == j:
            g.cmd &"""
                set label '{propI}' center at graph 0.5, graph 0.5 font ',18'
                set xrange [0:1]
                set yrange [0:1]
                plot NaN notitle
                unset label
                unset xrange
                unset yrange
            """
        # Otherwise it's a normal plot
        else:
            # Plot the three classes separately by filtering using the gnuplot ternary operator
            let plotElements = collect:
                for class in CLASSES:
                    &"u '{propJ}':(stringcolumn('class') eq '{class}' ? column('{propI}') : 1/0) w points ps 0.5 pt {COL_POINT_TYPES[class]} lc rgb '{COL_COLORS[class]}' title '{COL_NAMES[class]}'"

            # Only the first plot displays the overall key
            if not displayedKey:
                g.cmd "set key vertical at screen 0.46, screen 0.965 center top Right samplen -1 maxrows 1 width -2"
                g.plotData("iris", plotElements)
                g.cmd "unset key"
                displayedKey = true
            else:
                g.plotData("iris", plotElements)


let svgBytes = g.execute()
writeFile("iris.svg", svgBytes)
